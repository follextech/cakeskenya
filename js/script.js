// Function to render the product grid
function renderProductGrid(id,array) {
    const productGrid = document.getElementById(id);

    array.forEach((product) => {
        const gridItem = document.createElement("div");
        gridItem.classList.add("grid-item");

        const productImage = document.createElement("img");
        productImage.src = product.image;
        productImage.alt = product.name;

        const productName = document.createElement("p");
        productName.textContent = product.name;

        const productPrice = document.createElement("p");
        productPrice.textContent = product.price;

        const quantityInput = document.createElement("input");
        quantityInput.type = "number";
        quantityInput.min = 1;
        quantityInput.value = 1;

        const whatsappButton = document.createElement("button");
        whatsappButton.classList.add("button");
        whatsappButton.textContent = "Order on WhatsApp";

        whatsappButton.addEventListener("click", () => {
            const quantity = quantityInput.value;
            const message = `I want ${quantity} ${product.name} please and the price will add up to ${quantity*parseInt(product.price)}`;
            const phoneNumber = "+254712244969"; // Replace with your WhatsApp number
            const whatsappLink = `https://api.whatsapp.com/send?phone=${phoneNumber}&text=${encodeURIComponent(message)}`;

            window.location.href = whatsappLink;
        });

        gridItem.appendChild(productImage);
        gridItem.appendChild(productName);
        gridItem.appendChild(productPrice);
        gridItem.appendChild(quantityInput);
        gridItem.appendChild(whatsappButton);

        productGrid.appendChild(gridItem);
    });
}

